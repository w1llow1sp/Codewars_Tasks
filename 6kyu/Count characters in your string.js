{/*
* Основная идея заключается в подсчете всех встречающихся символов в строке.
* Если у вас есть строка типа aba, то результат должен быть {'a': 2, 'b': 1}.

Что делать, если строка пуста? Тогда результатом должен быть пустой литерал объекта {}.*/}

function count(string) {
    const result = {};

    // Обработка пустой строки
    if (string === '') {
        return result;
    }

    // Перебираем символы в строке и увеличиваем счетчик для каждого символа
    for (const char of string) {
        // Если символ уже встречался, увеличиваем счетчик, иначе устанавливаем в 1
        result[char] = (result[char] || 0) + 1;
    }

    return result;
}

// Пример использования
const result = count('aba');
console.log(result); // Вывод: { 'a': 2, 'b': 1 }
