/**
 * Ребенок играет с мячом на n-ом этаже высокого здания.
 * Высота этого этажа над уровнем земли, h, известна.
 * Он бросает мяч из окна. Мяч отскакивает (например) на две трети своей высоты (отскок 0,66).
 *
 * Его мать выглядывает из окна, расположенного на высоте 1,5 м от земли.
 * Сколько раз мать увидит, как мяч пролетит перед ее окном (в том числе во время падения и отскока)?
 *
 * Для корректного эксперимента должны быть выполнены три условия:
 * плавающий параметр "h" в метрах должен быть больше 0
 * плавающий параметр "bounce" должен быть больше 0 и меньше 1
 * плавающий параметр "window" должен быть меньше h.
 *
 * Если все три условия выполнены, верните целое положительное число, иначе верните -1.
 * Примечание: Мяч можно увидеть, только если высота отскочившего мяча строго больше параметра window. Примеры:
 * - h = 3, bounce = 0.66, window = 1.5, result is 3
 *
 * - h = 3, bounce = 1, window = 1.5, result is -1
 */

function bouncingBall(h, bounce, window) {
    if (h <= 0 || bounce <= 0 || bounce >= 1 || window >= h) return -1;

    let count = 1; // Первое падение сразу считаем
    let height = h * bounce;

    while (height > window) {
        count += 2; // Каждый отскок - это два пролёта мимо окна (вверх и вниз)
        height *= bounce;
    }

    return count;
}

bouncingBall(3,0.66,1.5)
bouncingBall(3,1,1.5)
