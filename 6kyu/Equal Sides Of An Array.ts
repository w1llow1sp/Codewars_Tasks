export function findEvenIndex(arr: number[]): number {
    for (let i =  0; i < arr.length; i++) {
        // Вычисляем сумму элементов слева от i
        let leftSum = arr.slice(0, i).reduce((a, b) => a + b,  0);
        // Вычисляем сумму элементов справа от i
        let rightSum = arr.slice(i +  1).reduce((a, b) => a + b,  0);
        // Если суммы равны, возвращаем индекс i
        if (leftSum === rightSum) {
            return i;
        }
    }
    // Если такого индекса нет, возвращаем -1
    return -1;
}


